import re

content = """Задачами работы являются:

> ● определить требования к данным с помощью диаграммы «сущность ‑
> связь» (использовать его как инструмент анализа требований к данным);
>
> ● научиться с помощью словаря данных описывать подробную картину
> данных системы;
>
> ● выполнить анализа данных с помощью матрицы CRUD.

# Список использованных источников и литературы

> 1\. Ахмедова, Х. Г. Обоснование и разработка требований к программным
> системам : учебное пособие / Х. Г. Ахмедова. — Москва : РТУ МИРЭА,
> 2023. — 104 с. — Текст : электронный // Лань :
> <https://e.lanbook.com/book/382694\>
>
> 2\. Завьялов, А. В. Диаграммы UML для анализа и проектирования
> информационных систем : учебно‑методическое пособие / А. В. Завьялов.
> — Москва : РТУ МИРЭА, 2021. — 65 с. — Текст : электронный // Лань :
> <https://e.lanbook.com/book/218630\>"""

lines = content.split('\n')
new_lines = []
in_list = False

for line in lines:
    if line.startswith("> "):
        line = line[2:]
        if line.startswith("● "):
            line = "- " + line[2:]
        elif line == "":
            line = ""
        else:
            # Need to align the continuation line
            if not re.match(r'^\d+\\\.', line): # if not a numbered list like 1\.
                line = "  " + line
    elif line.startswith(">"):
        line = line[1:]
    
    new_lines.append(line)
    
print("\n".join(new_lines))
